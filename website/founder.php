  <h1><font color="#b71c1c">Founders</font></h1>
  <h2>Shri Rameshwardas Birla (1892-1973)</h2> 
  <br>
  <font size="3px">
<img src="images/founder1.jpg" alt="alt text" style="float: left;height: 250px;width:170px;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Shri Rameshwardasji Birla, in whose memory the Endowment has been set up,
 was a leading industrialist and philanthropist who dedicated his life to medical, social, cultural, educational and religious activities all over 
India. He was the pioneering spirit behind the Bombay Hospital and was the Founder Chairman of its Trust.
His humanitarian approach and untiring efforts were responsible for setting up of the Bombay Hospital in 1950 with 440 – bed capacity. 
Later, in 1972 a 15-storey Medical Research Centre (Now known as the R. D. Birla Tower) was added catering to all specialities in the medical field, and equipped with the most sophisticated equipment, and with a strength of over 200 beds, making a total of 640 beds. With the completion of the 15-storey New Wing of the Medical Research Centre (Now known as the M. P. Birla Tower) 
The bed strength of the Hospital has now gone up to 750 beds, of which 254 beds are in General Ward Categery.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;To the social and cultural life of the city of Bombay, 
Shri R. D. Birla gave the magnificent and spacious Birla Matushri Sabhagar in memory of his mother and the Birla Kreeda Kendra, at Chowpatty Seaface, in memory of his father.
 In addition, Shri Rameshwardasji Birla set up hospitals at Pilani,
 Calcutta and other centres, apart from several charitable dispensaries. He passed away on April 21, 1973
</p>
<br>
<h2>Shri Madhav Prasad Birla (1918-1990)</h2> 
<p>

<img src="images/founder2.jpg" alt="alt text" style="float: left;height: 250px;width:170px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Shri Madhav Prasad Birla, Vice-Chairman of the Bombay Hospital Trust was born in Bombay on the 4th July, 1918. Shri M. P. Babu, as he was universally and affectionately known, 
was a man of great enterprise, who set up a host of companies like Birla Corp, Universal Cables, Vindhya Telelinks, Hindustan Gum & Chemicals, Digvijay Woollen Mills, 
Indian Smelting, etc., all leaders in their own fields, manufacturing quality products. He also established Birla Planetorium & Belle Vue Clinic and several schools in Calcutta.
Shri M. P. Babu will be remembered as a great philanthropist rather than as a leading and eminent industrialist.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
 Totally devoid of ego, and instinctively averse to self-advertisement, he gave profusely and generously to charitable causes spread over 
the length and breadth of the country, without ever expecting or allowing his name to be associated with his munificent donations. 
He established the M. P. Birla Foundation in fulfilment of his deep commitment to furthering progress in the fields of education and medicine.
 Shri M. P. Birla was also intimately associated with the famous Birla Institute of Technology & Science at Pilani.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Bombay Hospital enjoyed a very special place in his heart. He liberally gave crores of rupees to this great Institution. 
He took keen interest in the working of the Hospital and made frequent enquiries about the quality of treatment, more particularly to the poor and needy.
 He was responsible in no small measure, for the growth and development of the Hospital, set up by his illustrious father Shri Rameshwardasji Birla, 
into a 750 bedded Hospital, one of the largest and best equipped in the country.
</p>
<br>
<h2>Smt. Priyamvada Devi Birla (1928-2004)</h2> 

<p>
<img src="images/founder3.jpg" alt="alt text" style="float: left;height: 250px;width:170px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Smt. Priyamvada Devi Birla was a unique and unconventional person, who devoted her time, energy and wealth for the betterment of our Society.
 She was also a very devoted wife and did her best to maintain the legacy of her late husband Shri. M. P. Birla.
Hospitals, schools & temples form a very large and integral part of the M P Birla group which continued the tradition set by the late 
Shri M P Birla and his wife late Smt. Priyamvada Devi Birla.
</p>
<br><br><br><br>

<h2>Shri B.K.Taparia (1946)</h2> 
<p>

<img src="images/founder4.jpg" alt="alt text" style="float: left;height: 250px;width:170px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Shri Bharat Taparia is the son of late Shri Narendra Singh Taparia (son of late Rai Bahadur Himmat Singhji Maheshwari) and late Smt.
 Anusuya Devi Taparia (daughter of late Shri G. D. Birla). He was born on 5th August, 1946 at Jaipur in the House of his paternal grandfather 
who was the revenue minister at that time in the princely state of Jaipur.
After completing his studies from Mumbai, he entered business in the year 1971 and starting from scratch has built up a business conglomerate
 of six privately held companies having diverse business with a combined net worth in excess of Rs. 1000 crores.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Mr. Taparia’s real passion however is managing Bombay Hospital Trust in which he was inducted as Deputy Chairman in the year 1984 by his uncle late 
Shri M. P. Birla. Shri Taparia believes that charity and the pursuit of excellence are the two fundamental ideals which provide the impetus to focus on the well being
 of the patient. His vision is to provide the best possible treatment, delivered most efficiently in the shortest span of time and at minimum cost.
Over the last twenty-five years in his capacity as Deputy / Vice Chairman and now Chairman since 1992, he has radically
 transformed the working of the Hospital by inducting professionalism and introducing the most modern concepts of management.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Even though Bombay Hospital Trust is sixty years old, the Hospital’s 600,000 sq. ft has been totally refurbished and is equipped with the most modern equipment 
numbering 1422 of which the average age is seven years. The systems are computerized enabling the management to avail all required information on a real time basis.
In the year 1988, under the supervision of Shri Bharat Taparia, a new wing, now known as the M.P. Birla Tower, having 225 beds, 
state-of-the-art Library, Museum & Board Room was commissioned at Bombay Hospital, Mumbai.
</p>
